#include <iostream>
using namespace std;

// The char data type is used to store a single character. 
// The character must be surrounded by single quotes, like 'A' or 'c':
// you can use ASCII values to display certain characters

int main() {
    char a = 'X';
    char b = 65;
    char c = 66;

    cout << a << "\n" << b << "\n" << c;
    return 0;
}